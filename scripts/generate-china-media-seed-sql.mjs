import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { dirname, resolve } from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { validateChinaMediaSeedRows } from "./validate-china-media-seed.mjs";

const defaultOutputPath = "supabase/seed/202607100002_china_media_ecosystem_seed.sql";
const dollarQuoteTag = "pgos_china_media_seed_v0_1";

function parseArgs(argv) {
  const args = {
    packagePath: process.env.PGOS_CHINA_MEDIA_SEED_PACKAGE,
    jsonPath: null,
    outputPath: defaultOutputPath,
    checkOnly: false
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--package") {
      args.packagePath = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === "--json") {
      args.jsonPath = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === "--output") {
      args.outputPath = argv[index + 1];
      index += 1;
      continue;
    }

    if (arg === "--check-only") {
      args.checkOnly = true;
    }
  }

  return args;
}

function resolveDefaultPackagePath(root) {
  const candidates = [
    process.env.PGOS_CHINA_MEDIA_SEED_PACKAGE,
    resolve(root, "PGOS_China_Media_Ecosystem_Seed_V0.1_Package.zip"),
    resolve(homedir(), "Documents", "PGOS_China_Media_Ecosystem_Seed_V0.1_Package.zip")
  ].filter(Boolean);

  return candidates.find((candidate) => existsSync(candidate));
}

function readSeedJsonFromPackage(packagePath) {
  const result = spawnSync("tar", ["-xOf", packagePath, "PGOS_China_Media_Ecosystem_Seed_V0.1.json"], {
    encoding: "utf8"
  });

  if (result.status !== 0) {
    throw new Error(result.stderr.trim() || `Failed to read seed JSON from ${packagePath}.`);
  }

  return result.stdout;
}

function loadRows(root, args) {
  if (args.jsonPath) {
    const jsonPath = resolve(root, args.jsonPath);
    return {
      source: jsonPath,
      rows: JSON.parse(readFileSync(jsonPath, "utf8"))
    };
  }

  const packagePath = args.packagePath ?? resolveDefaultPackagePath(root);
  if (!packagePath || !existsSync(packagePath)) {
    throw new Error(
      "China media seed package was not found. Pass --package <zip-path> or set PGOS_CHINA_MEDIA_SEED_PACKAGE."
    );
  }

  return {
    source: packagePath,
    rows: JSON.parse(readSeedJsonFromPackage(packagePath))
  };
}

export function buildChinaMediaSeedSql(rows, options = {}) {
  const validation = validateChinaMediaSeedRows(rows, { expectedCount: options.expectedCount ?? 468 });
  if (!validation.ok) {
    throw new Error(`Seed validation failed before SQL generation:\n${validation.failures.join("\n")}`);
  }

  const jsonPayload = JSON.stringify(rows, null, 2);
  if (jsonPayload.includes(`$${dollarQuoteTag}$`)) {
    throw new Error("Seed JSON unexpectedly contains the SQL dollar-quote tag.");
  }

  return `-- Phase CM-1D: China Media Ecosystem controlled seed import.
-- Generated by scripts/generate-china-media-seed-sql.mjs.
-- Safe to re-run: opportunities are upserted on seed_id and seed_import logs are inserted once per opportunity.
-- This seed only writes media_ecosystem_opportunities and media_ecosystem_conversion_logs.
-- It does not write publishers, trusted_supply_candidates, deals, PMP, Preferred Deals, or Programmatic Guaranteed records.

with seed_rows as (
  select *
  from jsonb_to_recordset($${dollarQuoteTag}$
${jsonPayload}
$${dollarQuoteTag}$::jsonb) as row_data(
    seed_id text,
    media_name text,
    source_primary_segment_cn text,
    source_secondary_category_cn text,
    pg_ecosystem_segment_code text,
    pg_ecosystem_segment_cn text,
    media_type_initial text,
    primary_scene_initial text,
    ad_formats_if_known text,
    potential_integration_methods text,
    ecosystem_status text,
    verification_status text,
    data_quality_level text,
    trust_status text,
    trusted_supply_candidate text,
    deal_ready_status text,
    recommended_trading_mode text,
    priority_level_seed text,
    owner_role_initial text,
    owner_id text,
    next_action text,
    forbidden_commitments text,
    trusted_supply_link_rule text,
    pmp_trading_link_rule text,
    source_name text,
    source_version text,
    source_file text,
    source_page integer,
    seed_confidence text,
    import_batch_id text,
    review_required text,
    notes text
  )
),
upserted as (
  insert into public.media_ecosystem_opportunities (
    seed_id,
    media_name,
    source_primary_segment_cn,
    source_secondary_category_cn,
    ecosystem_segment,
    ecosystem_segment_cn,
    media_type_initial,
    primary_scene_initial,
    ad_formats_if_known,
    potential_integration_methods,
    ecosystem_status,
    verification_status,
    data_quality_level,
    trust_status,
    trusted_supply_candidate,
    deal_ready_status,
    recommended_trading_mode,
    seed_priority_level,
    priority_level,
    owner_role,
    next_action,
    review_required,
    seed_confidence,
    import_batch_id,
    source_name,
    source_version,
    source_file,
    source_page,
    forbidden_commitments,
    trusted_supply_link_rule,
    pmp_trading_link_rule,
    notes,
    metadata
  )
  select
    seed_id,
    media_name,
    source_primary_segment_cn,
    source_secondary_category_cn,
    pg_ecosystem_segment_code,
    pg_ecosystem_segment_cn,
    media_type_initial,
    primary_scene_initial,
    ad_formats_if_known,
    potential_integration_methods,
    ecosystem_status,
    verification_status,
    data_quality_level,
    trust_status,
    false,
    deal_ready_status,
    recommended_trading_mode,
    priority_level_seed,
    'UNSCORED',
    owner_role_initial,
    next_action,
    upper(review_required) = 'TRUE',
    seed_confidence,
    import_batch_id,
    source_name,
    source_version,
    source_file,
    source_page,
    forbidden_commitments,
    trusted_supply_link_rule,
    pmp_trading_link_rule,
    nullif(notes, ''),
    jsonb_build_object(
      'seed_import_source', 'PGOS_China_Media_Ecosystem_Seed_V0.1_Package.zip',
      'seed_import_mode', 'controlled_seed_only',
      'source_primary_segment_cn', source_primary_segment_cn,
      'source_secondary_category_cn', source_secondary_category_cn,
      'owner_id_from_seed', nullif(owner_id, '')
    )
  from seed_rows
  on conflict (seed_id) do update
  set media_name = excluded.media_name,
      source_primary_segment_cn = excluded.source_primary_segment_cn,
      source_secondary_category_cn = excluded.source_secondary_category_cn,
      ecosystem_segment = excluded.ecosystem_segment,
      ecosystem_segment_cn = excluded.ecosystem_segment_cn,
      media_type_initial = excluded.media_type_initial,
      primary_scene_initial = excluded.primary_scene_initial,
      ad_formats_if_known = excluded.ad_formats_if_known,
      potential_integration_methods = excluded.potential_integration_methods,
      seed_priority_level = excluded.seed_priority_level,
      owner_role = excluded.owner_role,
      next_action = excluded.next_action,
      review_required = excluded.review_required,
      seed_confidence = excluded.seed_confidence,
      import_batch_id = excluded.import_batch_id,
      source_name = excluded.source_name,
      source_version = excluded.source_version,
      source_file = excluded.source_file,
      source_page = excluded.source_page,
      forbidden_commitments = excluded.forbidden_commitments,
      trusted_supply_link_rule = excluded.trusted_supply_link_rule,
      pmp_trading_link_rule = excluded.pmp_trading_link_rule,
      notes = excluded.notes,
      metadata = media_ecosystem_opportunities.metadata || excluded.metadata,
      updated_at = now()
  returning id, seed_id, ecosystem_status, owner_role, import_batch_id
),
logged as (
  insert into public.media_ecosystem_conversion_logs (
    opportunity_id,
    from_status,
    to_status,
    conversion_type,
    conversion_reason,
    created_by_role,
    metadata
  )
  select
    opportunity.id,
    null,
    opportunity.ecosystem_status,
    'seed_import',
    'Controlled import from PGOS China Media Ecosystem Seed V0.1. Seed opportunities are not trusted supply.',
    opportunity.owner_role,
    jsonb_build_object(
      'seed_id', opportunity.seed_id,
      'import_batch_id', opportunity.import_batch_id,
      'safe_import', true
    )
  from upserted opportunity
  where not exists (
    select 1
    from public.media_ecosystem_conversion_logs existing
    where existing.opportunity_id = opportunity.id
      and existing.conversion_type = 'seed_import'
  )
  returning id
)
select
  (select count(*) from upserted) as opportunities_upserted,
  (select count(*) from logged) as seed_import_logs_inserted;
`;
}

function main() {
  const root = process.cwd();
  const args = parseArgs(process.argv.slice(2));
  const loaded = loadRows(root, args);
  const sql = buildChinaMediaSeedSql(loaded.rows);
  const outputPath = resolve(root, args.outputPath);

  if (!args.checkOnly) {
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, sql, "utf8");
  }

  const validation = validateChinaMediaSeedRows(loaded.rows);
  console.log("China media ecosystem seed SQL generation passed.");
  console.log(`Source: ${loaded.source}`);
  console.log(`Rows: ${validation.summary.totalRows}`);
  console.log(`Output: ${args.checkOnly ? "(check only)" : outputPath}`);
  console.log("Safe import mode: seed opportunities only, no publisher/trusted/deal writes.");

  for (const warning of validation.warnings) {
    console.warn(`Warning: ${warning}`);
  }
}

