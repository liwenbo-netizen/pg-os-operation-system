      null::text,
      refs.advertiser_daily_yoga_id::text,
      'finance_review',
      'sales_director',
      'low',
      'USD',
      20000::numeric,
      '2026-07-01'::date,
      '2026-09-30'::date,
      'Finance needs to verify payment terms.',
      'Finance review payment terms.',
      null::text,
      null::text,
      null::text
    ),
    (
      '11111111-1111-4111-8111-111111111003'::uuid,
      'publisher',
      refs.publisher_233_id,
      'CON-003',
      '233',
      'settlement_side_letter',
      refs.publisher_233_id::text,
