-- Phase 38 contract UAT bootstrap.
-- Rehydrates the Contract Workspace with deterministic UAT contracts when the
-- live Supabase contracts table is empty or missing the contract mainline seed.

with refs as (
  select
    (select id from public.profiles where email = 'legal_manager@poly-gamma.com' limit 1) as legal_user_id,
    (select id from public.publishers where name = '233' limit 1) as publisher_233_id,
    (select id from public.publishers where name = 'LOFTER' limit 1) as publisher_lofter_id,
    (select id from public.advertisers where name = 'Daily Yoga' limit 1) as advertiser_daily_yoga_id
)
insert into public.contracts (
  id,
  object_type,
  object_id,
  contract_name,
  counterparty,
  owner_user_id,
  status,
  effective_date,
  expiry_date,
  metadata,
  created_by,
  updated_by
)
select
  contract_seed.id,
  contract_seed.object_type,
  contract_seed.object_id,
  contract_seed.contract_name,
  contract_seed.counterparty,
  refs.legal_user_id,
  contract_seed.status,
  contract_seed.effective_date,
  contract_seed.expiry_date,
  jsonb_strip_nulls(
    jsonb_build_object(
      'contract_no', contract_seed.contract_name,
      'contract_type', contract_seed.contract_type,
      'counterparty_name', contract_seed.counterparty,
      'publisher_id', contract_seed.publisher_id,
      'advertiser_id', contract_seed.advertiser_id,
      'owner_role', 'legal_manager',
      'requested_by_role', contract_seed.requested_by_role,
      'risk_level', contract_seed.risk_level,
      'currency', contract_seed.currency,
      'value_amount', contract_seed.value_amount,
      'blocker', contract_seed.blocker,
      'next_action', contract_seed.next_action,
      'finance_notes', contract_seed.finance_notes,
      'signed_at', contract_seed.signed_at,
      'archived_at', contract_seed.archived_at
    )
  ),
  refs.legal_user_id,
  refs.legal_user_id
from refs
cross join lateral (
  values
    (
      '11111111-1111-4111-8111-111111111001'::uuid,
      'publisher',
      refs.publisher_233_id,
